#include "3-calc.h"
/**
 * op_add - adds
 * @a: first int
 * @b: the other int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substracts
 * @a: first int
 * @b: other int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multplies
 * @a: first int
 * @b: other int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides
 * @a: first int
 * @b: other int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: first int
 * @b: other int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
